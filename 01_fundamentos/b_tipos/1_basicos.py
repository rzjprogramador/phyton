# CONCEITO: OS TIPOS EM PY S√ÉO CLASSES:

print(type(1))       # NUMERO INTEIRO  :: <class 'int'>    :: int
print(type(9.2))     # NUMERO_DECIMAL  :: <class 'float'>  :: float
print(type('texto')) # CARACTERE       :: <class 'str'>    :: str
print(type(False))   # LOGICO          :: <class 'bool'>   :: bool
print(type(True))    # LOGICO          :: <class 'bool'>   :: bool

